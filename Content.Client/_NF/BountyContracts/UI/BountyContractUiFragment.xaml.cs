using Content.Shared.StationBounties;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;

namespace Content.Client._NF.BountyContracts.UI;

[GenerateTypedNameReferences]
public sealed partial class BountyContractUiFragment : BoxContainer
{
    public void ShowSubmenu(BountyContractFragmentState state)
    {
        switch (state)
        {
            case BountyContractFragmentState.List:
                ListMenu.Visible = true;
                CreateMenu.Visible = false;
                break;
            case BountyContractFragmentState.Create:
                ListMenu.Visible = false;
                CreateMenu.Visible = true;
                break;
        }
    }
}
